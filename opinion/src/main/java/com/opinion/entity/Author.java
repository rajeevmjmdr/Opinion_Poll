package com.opinion.entity;

public class Author {

	String author_Id;
	String author_Name;
	int author_votes;

	public String getAuthor_Id() {
		return author_Id;
	}

	public void setAuthor_Id(String author_Id) {
		this.author_Id = author_Id;
	}

	public String getAuthor_Name() {
		return author_Name;
	}

	public void setAuthor_Name(String author_Name) {
		this.author_Name = author_Name;
	}

	public int getAuthor_votes() {
		return author_votes;
	}

	public void setAuthor_votes(int author_votes) {
		this.author_votes = author_votes;
	}

}
